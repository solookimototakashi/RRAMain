def ImgCheck(FolURL2, FileName, conf, LoopVal):  # 画像があればTrueを返す関数
    ImgURL = FolURL2 + "/" + FileName
    for x in range(LoopVal):
        try:
            p = locateOnScreen(ImgURL, confidence=conf)
            x, y = center(p)
            return True, x, y
        except:
            Flag = 0
    if Flag == 0:
        return False, "", ""


