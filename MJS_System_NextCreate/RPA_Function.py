def getFileEncoding(file_path):
    if os.path.isfile(file_path) is True:
        detector = UniversalDetector()
        with open(file_path, mode="rb") as f:
            for binary in f:
                detector.feed(binary)
                if detector.done:
                    break
        detector.close()
        return detector.result["encoding"]
    else:
        return ""
